"""
Canonical country identity module.

This is the single source of truth for:
  1. Country display names (ISO-3 -> clean, consistent English name)
  2. Continent/region assignments (ISO-3 -> region code)

WHY THIS EXISTS
---------------
The three data sources use different country name strings for the same country:
  - UN SDG:     "Bolivia (Plurinational State of)", "Cote d'Ivoire", "Eswatini"
  - ND-GAIN:    "Bolivia, Plurinational State of",  "Cote d'Ivoire", "Swaziland"
  - World Bank: "Bolivia",                          "Cote d'Ivoire", "Eswatini"

Country CODES (ISO alpha-3) are already harmonized across all sources by the
cleaning pipeline. Always join on country_code, never on country_name.
Use get_canonical_name() for any display purposes.

POWER BI NOTE
-----------------------
  - Join tables on: country_code (ISO alpha-3, e.g. "AFG", "NGA")
  - Display using:  get_canonical_name(country_code) or COUNTRY_NAMES dict
  - Group by:       get_region(country_code) or COUNTRY_REGIONS dict
  - Never join on country_name — it differs across the source CSVs.
"""

