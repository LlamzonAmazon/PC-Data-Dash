def get_canonical_name(country_code: str, fallback: str = "") -> str:
    """Return the canonical display name for an ISO-3 country code.

    Args:
        country_code: ISO 3166-1 alpha-3 code (e.g. "AFG")
        fallback: Value to return if code is not found. Defaults to the code itself.

    Returns:
        Clean display name, e.g. "Afghanistan"
    """
    return COUNTRY_NAMES.get(country_code, fallback or country_code)


def get_region(country_code: str, fallback: str = "Unknown") -> str:
    """Return the continent code for an ISO-3 country code.

    Args:
        country_code: ISO 3166-1 alpha-3 code
        fallback: Value if code has no region mapping

    Returns:
        Continent code: "AF", "AS", "EU", "NA", "SA", or "OC"
    """
    return COUNTRY_REGIONS.get(country_code, fallback)


def get_region_name(region_code: str) -> str:
    """Return the full name for a region code.

    Args:
        region_code: One of "AF", "AS", "EU", "NA", "SA", "OC"

    Returns:
        Full region name, e.g. "Africa"
    """
    return REGION_NAMES.get(region_code, region_code)
